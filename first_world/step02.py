num1 = int(input('Write a number: '))
num2 = int(input('Write another number: '))

sum = num1 + num2
mult = num1 * num2
division = num1 / num2
integer_division = num1 // num2
exponentiation = num1 ** num2

print(f"The sum is {sum} - mult {mult} - division {division} - integer division {integer_division} - exponentiation {exponentiation}")
